package com.ui.model;

public class PropertyLayout {
  
  
  
  
  private int propertyLayoutId;
  private int propertyId;
  private String layoutTitle;
  private String image;
  private int createdBy;
  private String createdDate;
  private String ipAddress;
  public int getPropertyLayoutId() {
    return propertyLayoutId;
  }
  public void setPropertyLayoutId(int propertyLayoutId) {
    this.propertyLayoutId = propertyLayoutId;
  }
  public int getPropertyId() {
    return propertyId;
  }
  public void setPropertyId(int propertyId) {
    this.propertyId = propertyId;
  }
  public String getLayoutTitle() {
    return layoutTitle;
  }
  public void setLayoutTitle(String layoutTitle) {
    this.layoutTitle = layoutTitle;
  }
  public String getImage() {
    return image;
  }
  public void setImage(String image) {
    this.image = image;
  }
  public int getCreatedBy() {
    return createdBy;
  }
  public void setCreatedBy(int createdBy) {
    this.createdBy = createdBy;
  }
  public String getCreatedDate() {
    return createdDate;
  }
  public void setCreatedDate(String createdDate) {
    this.createdDate = createdDate;
  }
  public String getIpAddress() {
    return ipAddress;
  }
  public void setIpAddress(String ipAddress) {
    this.ipAddress = ipAddress;
  }

  
}
