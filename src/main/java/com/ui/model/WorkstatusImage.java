package com.ui.model;

public class WorkstatusImage {

  
  private int workstatusImageId;
  private int workstatusId;
  private String title;
  private String subtitle;
  private String image;
  private String status;
  private int createdBy;
  private String createdDate;
  private String ipAddress;
  public int getWorkstatusImageId() {
    return workstatusImageId;
  }
  public void setWorkstatusImageId(int workstatusImageId) {
    this.workstatusImageId = workstatusImageId;
  }
  public int getWorkstatusId() {
    return workstatusId;
  }
  public void setWorkstatusId(int workstatusId) {
    this.workstatusId = workstatusId;
  }
  public String getTitle() {
    return title;
  }
  public void setTitle(String title) {
    this.title = title;
  }
  public String getSubtitle() {
    return subtitle;
  }
  public void setSubtitle(String subtitle) {
    this.subtitle = subtitle;
  }
  public String getImage() {
    return image;
  }
  public void setImage(String image) {
    this.image = image;
  }
  public String getStatus() {
    return status;
  }
  public void setStatus(String status) {
    this.status = status;
  }
  public int getCreatedBy() {
    return createdBy;
  }
  public void setCreatedBy(int createdBy) {
    this.createdBy = createdBy;
  }
  public String getCreatedDate() {
    return createdDate;
  }
  public void setCreatedDate(String createdDate) {
    this.createdDate = createdDate;
  }
  public String getIpAddress() {
    return ipAddress;
  }
  public void setIpAddress(String ipAddress) {
    this.ipAddress = ipAddress;
  }
  
  
}
